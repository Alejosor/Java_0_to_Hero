public class julianb0315 {
    public static void main(String[] args) {
    //AND Lógico:
       boolean cond1 = false; 
       boolean cond2 = false;
       boolean and = cond1 && cond2;
       System.out.println("Logico AND: " + and);
    //OR Lógico:
       boolean cond3 = true;
       boolean cond4 = true;
       boolean or = cond3 || cond4;
       System.out.println("Logico OR: " + or);
    //NOT Lógico:
       boolean cond5 = true;
       boolean not = !cond5;
       System.out.println("Logico NOT: " + not);
    }
}
