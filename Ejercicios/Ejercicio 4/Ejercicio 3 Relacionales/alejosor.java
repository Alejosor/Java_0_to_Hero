public class alejosor {
    public static void main(String[] args) {
       // Operadores Relacionales
       int p = 8, q = 8;
       boolean igualdad = p == q;
       boolean desigualdad = p != q;
       System.out.println("Igualdad: " + igualdad);
       System.out.println("Desigualdad: " + desigualdad);

       int u = 12, v = 15;
       boolean mayor = u > v;
       boolean menorIgual = v <= u;
       System.out.println("Mayor: " + mayor);
       System.out.println("Menor o igual: " + menorIgual);
    }
}