public class julianb0315 {
    public static void main(String[] args) {
    //Comparación de Igualdad y Desigualdad:
       int p = 6; 
       int q = 30;
       boolean a = p == q;
       boolean b = p != q;
       System.out.println("Las variables son iguales: " + a);
       System.out.println("Las variables son desiguales: " + b);
    //Comparación Mayor y Menor:
       int u = 17; 
       int v = 2;
       boolean menor = u < v;
       boolean mayorIgual = v >= u;
       System.out.println("Es menor : " + menor);
       System.out.println("Es mayor o igual : " + mayorIgual);
    }
}
