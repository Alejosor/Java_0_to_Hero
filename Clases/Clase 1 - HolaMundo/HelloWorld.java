/**
 * Esto vendria siendo nuestro primer programa en Java, el famoso Hola Mundo o HelloWorld, aqui vemos tambien un poco
 * de la estructura de lo que viene siendo Java.
 *
 * public class HelloWorld {        <---- Esto es una Clase, todo lo que está dentro de la clase serán los “métodos”
 *     public static void main(String[] args) { <- Esto es un método; todo lo que esté dentro serán las "declaraciones"
 *         System.out.println("Hello World") <-- Y esto es un Declaración
 *     }
 * }
 */

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World");
    }
}

/*
  Datos importantes:
  - Toda declaración siempre debe acabar en punto y coma (;).
  - Para cualquier cosa que hagamos en Java, debemos implementar el método main.
 */
